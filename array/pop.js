// Array.prototype.pop()
// Метод pop() видаляє останній елемент з масиву
// і повертає цей елемент.
// Цей метод змінює довжину масиву

const colors = [
  'yellow',
  'blue',
  'green',
  'pink',
];

console.log(colors.pop());
// expected output: pink

console.log(colors);
// expected output: Array [ 'yellow', 'blue', 'green' ]
